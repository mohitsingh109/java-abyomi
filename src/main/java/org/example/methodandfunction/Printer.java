package org.example.methodandfunction;

// method or function
// AM class <class name>
public class Printer {

    // Function or Method
    //AM - public, private, protected, default
    //AM RT <FN>(Arg)
    public void print() {
        System.out.println("Hi i am printing something");
    }

    public void printWithArgs(String message) {
        System.out.println(message);
    }
}
