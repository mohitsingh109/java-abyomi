package org.example.datatype;

// OOPS
public class DataTypes {

    public static void main(String[] args) { // First function that run in java (JVM)

        // short
        short value = 9999; // age

        System.out.println(value);
        // int
        int age = 999999999; // (-23858)
        System.out.println(age);
        // long
        long amount = 10000000000L; //youtube views
        // float
        float value1 = 34.5f;
        System.out.println(value1);
        //double
        double value2 = 999999999.568;
        System.out.printf("%.1f",value2);
        // char
        char a = 'a';
        char b = 'b';

        // boolean
        boolean value3 = false; // true
        value3 = true;
    }
}
